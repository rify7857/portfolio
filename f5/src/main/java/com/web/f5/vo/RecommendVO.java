package com.web.f5.vo;

public class RecommendVO {
	String replyIdx, memberId, replyRecommendCheck;
	String boardIdx, boardRecommendCheck;
	public String getReplyIdx() {
		return replyIdx;
	}
	public void setReplyIdx(String replyIdx) {
		this.replyIdx = replyIdx;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getReplyRecommendCheck() {
		return replyRecommendCheck;
	}
	public void setReplyRecommendCheck(String replyRecommendCheck) {
		this.replyRecommendCheck = replyRecommendCheck;
	}
	public String getBoardIdx() {
		return boardIdx;
	}
	public void setBoardIdx(String boardIdx) {
		this.boardIdx = boardIdx;
	}
	public String getBoardRecommendCheck() {
		return boardRecommendCheck;
	}
	public void setBoardRecommendCheck(String boardRecommendCheck) {
		this.boardRecommendCheck = boardRecommendCheck;
	}
	
	
	
}
